package hourcalendar;

public class Grau {
    public String nom;
    public String codi;

    public Grau(String _nom, String _codi) {
        nom = _nom;
        codi = _codi;
    }
    
    public String getNom() {
        return nom;
    }
       
    public String getCodi() {
        return codi;
    }
}